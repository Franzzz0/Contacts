class Complex {

    private double real;
    private double imaginary;

    public double getReal() {
        return this.real;
    }

    public double getImaginary() {
        return this.imaginary;
    }

    public void setReal(double value) {
        this.real = value;
    }

    public void setImaginary(double value) {
        this.imaginary = value;
    }
}